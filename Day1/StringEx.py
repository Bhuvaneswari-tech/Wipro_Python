#String slicing
str1 = 'Hello World'

print(str1) #Hello World
print(str1[0]) #H
print(str1[2:5])#upto 5 index - 2,3,4
print(str1[4:]) #o World
print(str1[:])#Hello World
print(str1[::-1]) #reverse order - dlroW olleH
print(str1*2) #Print two times
print(str1[-4:-1]) #upto -1 
print(str1 + ' Test') #Hello World Test - concatenated string